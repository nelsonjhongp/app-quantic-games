    public void actualizarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) tablaHelp.getModel(); 
        modelo.setRowCount(0);
        modelo = obj.tablaPila(modelo);
        tablaHelp.setModel(modelo);
        TotalPagar();
    }
    
    public void RegistrarVenta(){
        String cliente = txtName.getText();
        String vendedor = txtVend.getText();
        double monto = totalpagar;
        obj.RegistrarVenta(cliente, vendedor, monto);
    }
    
    public void RegistrarDetalle(){
        int idventa=Venta.getContar();
        for(int i = 0; i < tablaHelp.getRowCount(); i++){
            int cod = Integer.parseInt(tablaHelp.getValueAt(i, 0).toString());
            int cant = Integer.parseInt(tablaHelp.getValueAt(i, 2).toString());
            double precio = Double.parseDouble(tablaHelp.getValueAt(i, 3).toString());
            
            obj.RegistrarDetalle(cod, cant,precio, idventa);
        }
    }
    
    private void actualizarStock(){
        for(int i = 0; i < tablaHelp.getRowCount(); i++){
            String cod = tablaHelp.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(tablaHelp.getValueAt(i, 2).toString());
            Producto p1 = new Producto();
            p1 = obj.BuscarPro(Integer.parseInt(cod));
            int stockAct = p1.getCantidad() - cant;
            obj.actualizarStock(stockAct, cod);
        }
    }
    
    public void TotalPagar(){
        totalpagar = 0;
        int numFila = tablaHelp.getRowCount();
        for(int i = 0; i < numFila; i++){
            double cal = Double.parseDouble(String.valueOf(tablaHelp.getModel().getValueAt(i, 4)));
            totalpagar = totalpagar + cal;
        }
        txtPaga.setText(String.format("%.2f", totalpagar));
    }
    
