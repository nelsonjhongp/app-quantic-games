    private void txtIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
                int cod = Integer.parseInt(txtID.getText());
                Producto pro = obj.BuscarPro(cod);
                if(pro.getInfo() != null){
                    txtDescripcionPro.setText("" + pro.getInfo());
                    txtPre.setText(""+pro.getPrecio());
                    txtStockDis.setText(""+ pro.getCantidad());
                    txtCant.requestFocus();
                } else {
                    txtDescripcionPro.setText("");
                    txtPre.setText("");
                    txtStockDis.setText("");
                    txtID.requestFocus();
                }
            
        }
        
    }//GEN-LAST:event_txtIDKeyPressed

    private void txtCantKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantKeyPressed
        
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCant.getText())){
                //Pila de IdProducto
                obj.PilaVenta(Integer.parseInt(txtID.getText()),Integer.parseInt(txtCant.getText()));
                
                int cod = Integer.parseInt(txtID.getText());
                String desc = txtDescripcionPro.getText();
                int cant = Integer.parseInt(txtCant.getText());
                double precio = Double.parseDouble(txtPre.getText());
                double total = cant * precio;
                int stock = Integer.parseInt(txtStockDis.getText());
                
                if(stock >= cant){
                    item = item +1;
                    pres = pres + 1;
                    actualizarTabla();
                    
                    LimpiarVenta();
                } else {
                    JOptionPane.showMessageDialog(null, "Stock no disponible");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese cantidad");
            }
        }
    
    }//GEN-LAST:event_txtCantKeyPressed

    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        boolean fieldDNI=true, fieldPago=true;
        if(txtDNI.getText().equals("")){fieldDNI=false;}
        if(txtPaga.getText().equals("") || Double.parseDouble(txtPaga.getText())==0){fieldPago=false;}
        
        System.out.println("txttDni: "+fieldDNI+" - txtPago: "+fieldPago);
        if (fieldDNI && fieldPago) {
            RegistrarDetalle();
            RegistrarVenta();
            actualizarStock();

            obj.eliminarTodoPila();
            
            DefaultTableModel model = (DefaultTableModel) tablaHelp.getModel();
            model.setRowCount(0);

            txtDNI.setText("");
            txtName.setText("");
            txtVend.setText("");
            txtPaga.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Falta rellenar datos en la venta!",
      "Faltan Datos!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnVenderActionPerformed

